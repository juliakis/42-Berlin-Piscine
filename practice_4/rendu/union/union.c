#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	if (ac != 2)
	{
		write(1, "\n", 1);
		return (0);
	}
	i = 0;
	while (av[1][i])
	{
		j = 0;
		while (j <= i)
		{
			if ((av[1][i] - av[1][i - j]) == 0)
				j++;
		}
		write (1, &av[1][i - j], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
